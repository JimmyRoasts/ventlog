<section class="space-y-6 rounded-lg border border-border bg-card p-6 shadow-sm text-card-foreground">
	<h1 class="text-2xl font-semibold tracking-tight">Guidance</h1>

	<div class="space-y-3">
		<h2 class="text-lg font-semibold">What is a ventilation survey?</h2>
		<ul class="list-disc space-y-1 pl-5 text-muted-foreground">
			<li>Snapshot of airflow, pressure, and gas conditions across key headings and return paths.</li>
			<li>Used to confirm design volumes, identify restrictions, and track changes over time.</li>
			<li>Should follow consistent routes and measurement points so results stay comparable.</li>
		</ul>
	</div>

	<div class="space-y-3">
		<h2 class="text-lg font-semibold">Instruments</h2>
		<ul class="list-disc space-y-1 pl-5 text-muted-foreground">
			<li>Hand-held anemometer with recent calibration sticker.</li>
			<li>Barometer/pressure gauge for differential readings across regulators and doors.</li>
			<li>Gas detector capable of O<sub>2</sub>, CO, CO<sub>2</sub>, and CH<sub>4</sub> where relevant.</li>
			<li>Compass and tape/laser for cross-sectional area estimates where plans are unavailable.</li>
		</ul>
	</div>

	<div class="space-y-3">
		<h2 class="text-lg font-semibold">Basic procedure</h2>
		<ul class="list-disc space-y-1 pl-5 text-muted-foreground">
			<li>Confirm survey route, tag nodes, and note fan/regulator states before starting.</li>
			<li>Take stable airflow readings (multiple passes if turbulent) and record duct sizes/areas.</li>
			<li>Log pressures across controls, fan settings, and any observed leakage paths.</li>
			<li>Capture gas checks in returns, exhaust raises, and sealed areas per site rules.</li>
			<li>Note hazards, obstructions, and any non-standard controls for follow-up actions.</li>
		</ul>
		<p class="text-sm text-muted-foreground">
			These notes are aligned with typical WA underground ventilation expectations and audit preparation, but
			they are not a substitute for site procedures or statutory requirements.
		</p>
	</div>
</section>
